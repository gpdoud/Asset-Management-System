export class Location {
	
	Id: number;
	Department: string;
	Address: string;
	City: string;
	State: string;
	Phone: string;
	
	
	constructor(
		Id: number,
		Department: string,
		Address: string,
		City: string,
		State: string,
		Phone: string,
				
	)

	 {
	 	this.Id = Id;
	  	this.Department = Department;
	 	this.Address = Address;
	 	this.City = City;
	 	this.State = State;
	 	this.Phone = Phone;
	 	 	
	 	}


}